#!/usr/bin/perl

$integer = 200;
$negative = -300;
$floating = 200.340;
$bigFloat = -1.2E-23;

# 377 octal, same as 255 decimal
$octal = 0377;

# FF hex, also 255 decimal
$hexa = 0xff;

print "integer = $integer\n";
print "negative = $negative\n";
print "floating = $floating\n";
print "bigFloat = $bigFloat\n";
print "octal = $octal\n";
print "hexa = $hexa\n";
